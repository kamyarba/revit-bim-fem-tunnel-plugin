        public void exportSelectedFaces()
        {
            TurUpAIRunGmsh.ClassMeshGeneration meshgenerator = new TurUpAIRunGmsh.ClassMeshGeneration();

            using (Transaction tx = new Transaction(uidoc.Document, "clear selection"))
            {
                tx.Start();
                uidoc.Selection.SetElementIds(new List<ElementId>());
                uidoc.RefreshActiveView();
                tx.Commit();
            }

            IList<Reference> references = uidoc.Selection.PickObjects(ObjectType.Face, "Select faces to export to Femix");
            
            StringBuilder GmshGeoFile = new StringBuilder("//## Generated by Kamyar_ba@yahoo.com \r\n");


            int curveCounter = 1;
            foreach (Reference reference in references) 
            {
                ElementId motherElementId = reference.ElementId;
                GeometryObject geometryObject = uidoc.Document.GetElement(reference).GetGeometryObjectFromReference(reference);
                Face face = geometryObject as Face;
                String area = face.Area.ToMeters().ToString("0.00");
                //TaskDialog.Show ("element id", $"{elementId.ToString()}");
                //String fileName = $"e{elementId}f{geometryObject.Id}";
                
                List<Edge> curvesOfFace = new List<Edge>();
                foreach (EdgeArray edgeArray in face.EdgeLoops)
                {
                    foreach (Edge edge in edgeArray)
                    {
                        curvesOfFace.Add(edge);
                    }                    
                }

                StringBuilder GmshFacegeometry = createSurfaceGmshGeometry(curvesOfFace, area, motherElementId, geometryObject.Id, curveCounter);
                GmshGeoFile.AppendLine(GmshFacegeometry.ToString());
                curveCounter += curvesOfFace.Count();
            }


            StringBuilder gmshMeshSeting = GmshMeshSeting();
            GmshGeoFile.AppendLine(gmshMeshSeting.ToString());

            string geofilePath = Path.GetDirectoryName(uidoc.Document.PathName) + "\\Geometry.geo";
            string meshDirPath = Path.GetDirectoryName(uidoc.Document.PathName);

            //if (File.Exists(geofilePath))
            //    File.Delete(geofilePath);
            //if (File.Exists(meshDirPath + "\\output_mesh.msh"))
            //    File.Delete(meshDirPath + "\\output_mesh.msh");

            try
            {
                using (StreamWriter writer = new StreamWriter(geofilePath))
                {
                    writer.Write(GmshGeoFile.ToString());
                }
            }
            finally
            {
                //meshgenerator.generateMesh(geofilePath);
                meshgenerator.run_GMSH_exe(geofilePath);
                //TaskDialog.Show("Info", $"Geometry is created");
            }

            GmshToFemix.ClassGMSHConvert gMSHConvertFemix = new GmshToFemix.ClassGMSHConvert();

            

            var (GROUP_NAMES, POINT_COORDINATES, ELEMENT_NODES, ELEMENT_PROPERTIES) =
                gMSHConvertFemix.ConvertMSHtoFemixmesh(meshDirPath+ "\\output_mesh.msh", null);

            string femixdatafile = $"{Path.GetDirectoryName(uidoc.Document.PathName)}\\femix_gl.dat";
            
            //if (File.Exists(femixdatafile))
            //    File.Delete(femixdatafile);

            using (StreamWriter writer = new StreamWriter(femixdatafile))
            {
                writer.WriteLine($"{GROUP_NAMES}\n\n{POINT_COORDINATES}\n\n{ELEMENT_NODES}\n\n{ELEMENT_PROPERTIES}");
            }
            TaskDialog.Show("Info", $"The selected items exported to:\r\n{femixdatafile}");

        }

